class SwitchCaseExercise
{
	public static void main(String[] args)
	{
		int a = 20;
		switch(a)
		{
			case 10:
				System.out.println("hi");
				break;
			case 20:
				System.out.println("Hello");
				break;
			case 30:
				System.out.println("welcome");
				break;
			case 40:
				System.out.println("thank YOu");
				break;
			default:
				System.out.println("BYE");
		}
	}
}